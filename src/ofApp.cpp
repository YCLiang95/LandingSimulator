//--------------------------------------------------------------
// setup scene, lighting, state and load geometry
//
void ofApp::setup(){

	current_camera = 0;
	bWireframe = false;
	bDisplayPoints = false;
	bAltKeyDown = false;
	bCtrlKeyDown = false;
	bRocketLoaded = false;
	bTerrainSelected = true;
//	ofSetWindowShape(1024, 768);
	cam.setDistance(10);
	cam.setNearClip(.1);
	cam.setFov(65.5);   // approx equivalent to 28mm in 35mm format
	ofSetVerticalSync(true);
	//cam.disableMouseInput();
	cam.enableMouseInput();
	cameras.push_back(&cam);
	cam2.setTarget(glm::vec3(0, 25, 0));
	cam2.setDistance(10);
	cam2.setNearClip(.1);
	cam2.setFov(65.5);
	cam2.disableMouseInput();
	cameras.push_back(&cam2);
	ofEnableSmoothing();
	ofEnableDepthTest();

	// setup rudimentary lighting 
	//
	initLightingAndMaterials();

	mars.loadModel("geo/terrain_1.obj");
	mars.setScaleNormalization(false);
	rocket.loadModel("geo/rocket_1.obj");
	rocket.setScaleNormalization(false);
	bRocketLoaded = true;
	rocket.setPosition(0, -25, 0);

	ofSetBackgroundColor(ofColor::black);

	octree.create(mars.getMesh(0), 9);
}

//--------------------------------------------------------------
// incrementally update scene (animation)
//
void ofApp::update() {
	cam2.setTarget(glm::vec3(rocket.getPosition().x, - rocket.getPosition().y, rocket.getPosition().z));
}
//--------------------------------------------------------------
void ofApp::draw(){

//	ofBackgroundGradient(ofColor(20), ofColor(0));   // pick your own backgroujnd
	ofBackground(ofColor::black);
//	cout << ofGetFrameRate() << endl;

	cameras[current_camera]->begin();
	ofPushMatrix();
	ofMultMatrix(mars.getModelMatrix());
	if (bWireframe) {                    // wireframe mode  (include axis)
		ofDisableLighting();
		ofSetColor(ofColor::slateGray);
		mars.drawWireframe();
		if (bRocketLoaded) {
			rocket.drawWireframe();
			if (!bTerrainSelected) drawAxis(rocket.getPosition());
		}
		if (bTerrainSelected) drawAxis(ofVec3f(0, 0, 0));
	}
	else {
		ofEnableLighting();              // shaded mode
		mars.drawFaces();

		if (bRocketLoaded) {
			rocket.drawFaces();
			if (!bTerrainSelected) drawAxis(rocket.getPosition());
		}
		if (bTerrainSelected) drawAxis(ofVec3f(0, 0, 0));
	}


	if (bDisplayPoints) {                // display points as an option    
		glPointSize(3);
		ofSetColor(ofColor::green);
		mars.drawVertices();
	}
	ofPopMatrix();
	ofPushMatrix();

	// highlight selected point (draw sphere around selected point)
	//
	if (bPointSelected) {
		ofSetColor(ofColor::blue);
		ofDrawSphere(selectedPoint, .1);
	}
	ofSetColor(ofColor::white);
	ofNoFill();
	//octree.drawLeafNodes(octree.root);
	//octree.draw(7, 0);

	ofPopMatrix();
	cam.end();
}

